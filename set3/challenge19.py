english_chars = 'abcdefghijklmnopqrstuvxywz'
other_chars = "' ;:0123456789,.?!_-"
other_chars2 = "' ;:0123456789(),.?!+=-"


def attack(encrypted):
    key_stream_len = max([len(e) for e in encrypted])
    key_stream = bytearray(key_stream_len)
    matches = []
    for i in range(key_stream_len):
        matches.append([])
        for b in range(256):
            for e in encrypted:
                if i >= len(e):
                    continue
                c = chr(e[i] ^ b).lower()
                if c not in english_chars:
                    if c not in other_chars:
                        break
            else:
                matches[i].append(b)
                if len(matches[i]) > 5:
                    break

    combinations = itertools.product(*matches[:-5])
    best_score = 10000
    best_text = None
    for c in combinations:
        for i, v in enumerate(c):
            key_stream[i] = v
        plain_text = ""
        for e in encrypted:
            d = xor_bytes(e, key_stream[:len(e)])
            try:
                plain_text += bin2str(d)
            except UnicodeDecodeError:
                continue
        score = is_english(plain_text)
        if score < best_score:
            best_score = score
            best_text = plain_text
            print("new best: ", score, plain_text[:50])

    print(best_text)


def run():
    nonce = bytes(8)
    key = get_random_bytes(16)
